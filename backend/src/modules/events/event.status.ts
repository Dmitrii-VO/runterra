/**
 * Статусы событий
 * 
 * Описывает возможные состояния события в системе Runterra.
 * 
 * На текущей стадии (skeleton) содержит только enum без логики
 * и без бизнес-правил перехода между статусами.
 */

/**
 * Статус события
 */
export enum EventStatus {
  /** Событие в черновике (не опубликовано) */
  DRAFT = 'draft',
  
  /** Событие доступно для записи */
  OPEN = 'open',
  
  /** 
   * Нет свободных мест
   * 
   * Invariant: status === FULL ⇔ participantLimit != null && participantCount >= participantLimit
   * 
   * Статус FULL означает, что достигнут лимит участников.
   * Не может быть установлен вручную без проверки условия.
   */
  FULL = 'full',
  
  /** Событие отменено */
  CANCELLED = 'cancelled',
  
  /** Событие завершено */
  COMPLETED = 'completed',
}
