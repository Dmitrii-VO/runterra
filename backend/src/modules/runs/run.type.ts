/**
 * Типы для модуля пробежек
 * 
 * Описывает возможные статусы пробежки в системе Runterra.
 * 
 * На текущей стадии (skeleton) содержит только enum без логики
 * и без бизнес-правил для различных статусов.
 */

/**
 * Статус пробежки
 */
export enum RunStatus {
  /** Пробежка завершена успешно */
  COMPLETED = 'completed',
  
  /** Пробежка невалидна (слишком короткая, слишком быстрая и т.д.) */
  INVALID = 'invalid',
}
