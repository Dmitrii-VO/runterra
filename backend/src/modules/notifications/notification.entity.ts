/**
 * Сущность уведомления
 * 
 * Описывает модель уведомления в системе Runterra.
 * 
 * На текущей стадии (skeleton) содержит только структуру данных
 * без логики отправки и без работы с БД.
 * 
 * ВАЖНО:
 * - Нет логики отправки уведомлений
 * - Нет работы с БД
 * - Только контракты для будущей реализации
 */

import { NotificationType } from './notification.type';

/**
 * Интерфейс уведомления
 * 
 * Представляет полную модель уведомления в системе.
 * Все поля соответствуют будущей структуре БД.
 * 
 * ВАЖНО: Название UserNotification вместо Notification, чтобы избежать конфликта
 * с Web API Notification (браузерный API для уведомлений).
 */
export interface UserNotification {
  /** Уникальный идентификатор уведомления */
  id: string;
  
  /** Идентификатор пользователя-получателя */
  userId: string;
  
  /** Тип уведомления */
  type: NotificationType;
  
  /** Заголовок уведомления */
  title: string;
  
  /** Текст уведомления */
  message: string;
  
  /** Флаг прочтения уведомления */
  read: boolean;
  
  /** Дата создания уведомления */
  createdAt: Date;
}
