/**
 * Статусы активности
 * 
 * Описывает возможные состояния активности/тренировки в системе Runterra.
 * 
 * На текущей стадии (skeleton) содержит только enum без логики
 * и без бизнес-правил перехода между статусами.
 */

/**
 * Статус активности
 */
export enum ActivityStatus {
  /** Активность запланирована */
  PLANNED = 'planned',
  
  /** Активность в процессе */
  IN_PROGRESS = 'in_progress',
  
  /** Активность завершена */
  COMPLETED = 'completed',
  
  /** Активность отменена */
  CANCELLED = 'cancelled',
}
