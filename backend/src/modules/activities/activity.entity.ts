/**
 * Сущность активности
 * 
 * Описывает модель активности/тренировки в системе Runterra.
 * Активность представляет собой тренировку, которую пользователь может создать и выполнить.
 * 
 * На текущей стадии (skeleton) содержит только структуру данных
 * без логики, без GPS, без check-in, без расчётов, без работы с БД.
 * 
 * ВАЖНО:
 * - Нет GPS координат
 * - Нет check-in точек
 * - Нет расчётов дистанции, времени, скорости
 * - Нет маршрута
 * - Только контракты и структура данных
 */

import { ActivityType } from './activity.type';
import { ActivityStatus } from './activity.status';

/**
 * Интерфейс активности
 * 
 * Представляет полную модель активности в системе.
 * Все поля соответствуют будущей структуре БД.
 * 
 * ВАЖНО: Не содержит GPS данные, check-in, расчёты.
 * Это будет добавлено позже.
 */
export interface Activity {
  /** Уникальный идентификатор активности в системе */
  id: string;
  
  /** Идентификатор пользователя, создавшего активность */
  userId: string;
  
  /** Идентификатор события, к которому привязана активность (если есть) */
  eventId?: string;
  
  /** Тип активности */
  type: ActivityType;
  
  /** Статус активности */
  status: ActivityStatus;
  
  /** Название активности (опционально) */
  name?: string;
  
  /** Описание активности (опционально) */
  description?: string;
  
  /** Дата создания записи */
  createdAt: Date;
  
  /** Дата последнего обновления */
  updatedAt: Date;
}
