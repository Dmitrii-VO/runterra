/**
 * Роли участников в клубе
 *
 * Определяет роли пользователей в клубах системы Runterra.
 * Лидер, Тренер, Участник.
 */

/**
 * Роль участника в клубе
 */
export enum ClubRole {
  /** Участник клуба */
  MEMBER = 'member',

  /** Тренер клуба (создаёт тренировки, ведёт занятия) */
  TRAINER = 'trainer',

  /** Лидер клуба (основатель, управление клубом) */
  LEADER = 'leader',
}
