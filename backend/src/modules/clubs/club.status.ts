/**
 * Статусы клуба
 * 
 * Описывает возможные состояния клуба в системе Runterra.
 * 
 * На текущей стадии (skeleton) содержит только enum без логики
 * и без бизнес-правил перехода между статусами.
 */

/**
 * Статус клуба
 */
export enum ClubStatus {
  /** Клуб активен */
  ACTIVE = 'active',
  
  /** Клуб неактивен */
  INACTIVE = 'inactive',
  
  /** Клуб распущен */
  DISBANDED = 'disbanded',
  
  /** Клуб ожидает активации */
  PENDING = 'pending',
}
