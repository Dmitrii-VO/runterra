/**
 * Сущность связи пользователь-клуб
 * 
 * Описывает модель связи между пользователем и клубом с указанием роли.
 * Позволяет пользователю быть в нескольких клубах с разными ролями.
 * 
 * На текущей стадии (skeleton) содержит только структуру данных
 * без логики и без работы с БД.
 */

import { ClubRole } from './club-role';

/**
 * Интерфейс связи пользователь-клуб
 * 
 * Представляет полную модель связи между пользователем и клубом.
 * Все поля соответствуют будущей структуре БД.
 */
export interface ClubMembership {
  /** Уникальный идентификатор связи */
  id: string;
  
  /** Идентификатор пользователя */
  userId: string;
  
  /** Идентификатор клуба */
  clubId: string;
  
  /** Роль пользователя в клубе */
  role: ClubRole;
  
  /** Дата создания записи */
  createdAt: Date;
  
  /** Дата последнего обновления */
  updatedAt: Date;
}
