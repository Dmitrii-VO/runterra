/**
 * Сущность клуба
 * 
 * Описывает модель клуба в системе Runterra.
 * Клуб представляет собой объединение игроков для совместной игры.
 * 
 * На текущей стадии (skeleton) содержит только структуру данных
 * без логики, без ролей участников, без работы с БД.
 * 
 * ВАЖНО:
 * - Нет ролей участников
 * - Нет связей с пользователями
 * - Нет бизнес-логики
 * - Только контракты и структура данных
 */

import { ClubStatus } from './club.status';

/**
 * Интерфейс клуба
 * 
 * Представляет полную модель клуба в системе.
 * Все поля соответствуют будущей структуре БД.
 * 
 * ВАЖНО: Не содержит информацию об участниках и их ролях.
 * Это будет добавлено позже.
 */
export interface Club {
  /** Уникальный идентификатор клуба в системе */
  id: string;
  
  /** Название клуба */
  name: string;
  
  /** Описание клуба (опционально) */
  description?: string;
  
  /** Идентификатор города, в котором базируется клуб */
  cityId: string;
  
  /** Статус клуба */
  status: ClubStatus;
  
  /** Дата создания записи */
  createdAt: Date;
  
  /** Дата последнего обновления */
  updatedAt: Date;
}
