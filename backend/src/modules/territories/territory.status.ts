/**
 * Статусы территории
 * 
 * Описывает возможные состояния территории в системе Runterra.
 * 
 * На текущей стадии (skeleton) содержит только enum без логики
 * и без бизнес-правил перехода между статусами.
 */

/**
 * Статус территории
 */
export enum TerritoryStatus {
  /** Территория свободна (не захвачена) */
  FREE = 'free',
  
  /** Территория захвачена игроком */
  CAPTURED = 'captured',
  
  /** Территория оспаривается (в процессе захвата) */
  CONTESTED = 'contested',
  
  /** Территория заблокирована (временно недоступна) */
  LOCKED = 'locked',
}
