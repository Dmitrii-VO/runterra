/**
 * Сущность территории
 * 
 * Описывает модель территории в системе Runterra.
 * Территория представляет собой игровую зону, которую можно захватывать.
 * 
 * На текущей стадии (skeleton) содержит только структуру данных
 * без логики, без расчётов, без геометрии, без работы с БД.
 * 
 * ВАЖНО:
 * - Нет геометрии (полигоны, границы)
 * - Нет расчётов захвата
 * - Нет PostGIS
 * - Только контракты и структура данных
 */

import type { GeoCoordinates } from '../../shared/types/coordinates';
import { TerritoryStatus } from './territory.status';

/** Center coordinates of territory on map — alias for shared GeoCoordinates. */
export type TerritoryCoordinates = GeoCoordinates;

/**
 * Интерфейс территории
 * 
 * Представляет полную модель территории в системе.
 * Все поля соответствуют будущей структуре БД.
 * 
 * ВАЖНО: Не содержит геометрию границ, только координаты центра
 * для отображения на карте. Геометрия будет добавлена позже.
 */
export interface Territory {
  /** Уникальный идентификатор территории в системе */
  id: string;
  
  /** Название территории */
  name: string;
  
  /** Статус территории */
  status: TerritoryStatus;
  
  /** Координаты центра территории на карте */
  coordinates: TerritoryCoordinates;
  
  /** Идентификатор города, к которому относится территория */
  cityId: string;
  
  /** Идентификатор игрока, захватившего территорию (если захвачена) */
  capturedByUserId?: string;
  
  /** Дата создания записи */
  createdAt: Date;
  
  /** Дата последнего обновления */
  updatedAt: Date;
}
