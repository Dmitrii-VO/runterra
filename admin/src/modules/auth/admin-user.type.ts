import { AdminRole } from './admin.roles';

/**
 * Тип данных администратора в системе Runterra.
 * 
 * Описывает структуру данных пользователя административной панели.
 * Все поля обязательны для корректной работы системы авторизации.
 */
export type AdminUser = {
  /**
   * Уникальный идентификатор администратора.
   * Генерируется на backend при создании учетной записи.
   */
  id: string;

  /**
   * Email администратора.
   * Используется для входа в систему и уведомлений.
   */
  email: string;

  /**
   * Отображаемое имя администратора.
   * Используется в интерфейсе для идентификации пользователя.
   */
  name: string;

  /**
   * Роль администратора.
   * Определяет уровень доступа к функциям системы.
   */
  role: AdminRole;

  /**
   * Дата и время создания учетной записи.
   * Формат: ISO 8601 (YYYY-MM-DDTHH:mm:ss.sssZ)
   */
  createdAt: string;

  /**
   * Дата и время последнего входа в систему.
   * Формат: ISO 8601 (YYYY-MM-DDTHH:mm:ss.sssZ)
   * Может быть null, если пользователь еще не входил.
   */
  lastLoginAt: string | null;

  /**
   * Статус активности учетной записи.
   * false означает, что учетная запись заблокирована и вход недоступен.
   */
  isActive: boolean;
};

/**
 * Данные для входа администратора.
 * 
 * Используется при авторизации в системе.
 */
export type AdminLoginCredentials = {
  /**
   * Email администратора.
   */
  email: string;

  /**
   * Пароль администратора.
   * TODO: Определить требования к сложности пароля
   */
  password: string;
};
