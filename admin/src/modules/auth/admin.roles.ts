/**
 * Роли администраторов в системе Runterra.
 * 
 * Определяет уровни доступа для различных типов администраторов.
 * Используется для контроля доступа к функциям админ-панели.
 */

export enum AdminRole {
  /**
   * Супер-администратор.
   * Полный доступ ко всем функциям системы, включая управление другими администраторами.
   */
  SUPER_ADMIN = 'super_admin',

  /**
   * Администратор.
   * Доступ к большинству функций, кроме управления другими администраторами.
   */
  ADMIN = 'admin',

  /**
   * Модератор.
   * Ограниченный доступ: управление контентом, пользователями, но без доступа к системным настройкам.
   */
  MODERATOR = 'moderator',

  /**
   * Наблюдатель (Viewer).
   * Только просмотр данных без возможности редактирования.
   */
  VIEWER = 'viewer',
}

